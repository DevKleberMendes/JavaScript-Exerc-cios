// setTimeout (tempo para iniciar tarefa)
// 1 segundo = 1000
console.log("Antes do setTimeout");

setTimeout(function() {
    console.lot("Testando o setTimeout");

}, 2000);

console.log("Depois do setTimeout");


// setInterval (tempo de intervalo para executar tarefa = loop)
setInterval(function() {
    console.lot("Testando setInterval");

}, 1000);







